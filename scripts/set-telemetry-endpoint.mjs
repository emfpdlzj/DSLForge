import fs from 'node:fs';
import path from 'node:path';

const endpoint = (process.env.DSLFORGE_TELEMETRY_PROXY_ENDPOINT ?? '').trim();
const targetPath = path.resolve('src/generated/telemetryConfig.ts');
const nextContent = `// Generated by scripts/set-telemetry-endpoint.mjs.\n// Checked in with an empty default so local builds stay deterministic.\nexport const DEFAULT_TELEMETRY_ENDPOINT = ${JSON.stringify(endpoint)};\n`;

fs.mkdirSync(path.dirname(targetPath), { recursive: true });

const currentContent = fs.existsSync(targetPath) ? fs.readFileSync(targetPath, 'utf8') : undefined;

if (currentContent !== nextContent) {
  fs.writeFileSync(targetPath, nextContent, 'utf8');
}
